    public function importCSV(CriarTurmaRequest $request)
    {
        Excel::load($request->file('file'), function ($reader) {
            $reader->each(
                function($sheet) {
                    Usuario::firstOrCreate($sheet->toArray());
                }
            );
        });

        return redirect()->route('home');
